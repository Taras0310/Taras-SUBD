SELECT Матч.[Дата матча],Матч.[Код команди господарів], Матч.[Код команди гостей], Матч.[Забиті голи господарями],Матч.[Забиті гостями],
Стадіони.Назва, Стадіони.Місткість, Судді.[Головний суддя]
from (Матч INNER JOIN Стадіони ON Стадіони.Стадіон=Матч.Стадіон)
INNER JOIN Судді	 ON Судді.Бригада=Матч.[Суддівська бригада]
ORDER BY Місткість DESC;

SELECT  Країна, COUNT(Бригада) as Кількість from Судді
GROUP BY Судді.Країна;
